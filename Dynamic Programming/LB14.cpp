int power(ll a,ll b) {
    ll ans = 1;
    while(b>0){
        if(b%2){
            ans=ans*a;
        }
        a=a*a;b>>=1;
    }
    return ans;
}
ll countSetBits(ll n) {
    ll count = 0;
    while (n)
    {
        count += n & 1;
        n >>= 1;
    }
    return count;
}
 
ll gcd(ll a, string b){
    ll res = 0;
    for (int i = 0; i < b.length(); i++){
        res = ((res * 10) + (b[i] - '0')) % a;
    }
    return __gcd(a,res);
}
 
ll lcs(string a,string b,ll n,ll m){
    if(n == 0 || m == 0){
        return 0;
    }
    if(a[n-1] == b[m-1]){
        return 1+lcs(a,b,n-1,m-1);
    }
    else{
        return max(lcs(a,b,n-1,m),lcs(a,b,n,m-1));
    }
}

