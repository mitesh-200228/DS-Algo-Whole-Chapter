int power(ll a,ll b) {
    ll ans = 1;
    while(b>0){
        if(b%2){
            ans=ans*a;
        }
        a=a*a;b>>=1;
    }
    return ans;
}
ll countSetBits(ll n) {
    ll count = 0;
    while (n)
    {
        count += n & 1;
        n >>= 1;
    }
    return count;
}
 
ll gcd(ll a, string b){
    ll res = 0;
    for (int i = 0; i < b.length(); i++){
        res = ((res * 10) + (b[i] - '0')) % a;
    }
    return __gcd(a,res);
}
 
void solve(){
    ll n;cin>>n;
    
    std::vector<ll> dp(10e5,-1);
    dp[0] = 0;
    dp[1] = 1;
    for(ll i=2;i<=n;i++){
        dp[i] = max(i,dp[i/2] + dp[i/3] + dp[i/4]);
    }
    cout<<dp[n]<<endl;
}
 
