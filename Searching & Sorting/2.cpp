class Solution {
  public:
    int countSquares(int N) {
        // code here
        int x = 0;
        if(sqrt(N) == floor(sqrt(N))){
            x = sqrt(N)-1;   
        }else{
            x = floor(sqrt(N));
        }
        return x;
    }
};

