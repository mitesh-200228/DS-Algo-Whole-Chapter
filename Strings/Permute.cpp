int power(ll a,ll b) {
    ll ans = 1;
    while(b>0){
        if(b%2){
            ans=ans*a;
        }
        a=a*a;b>>=1;
    }
    return ans;
}
ll countSetBits(ll n) {
    ll count = 0;
    while (n)
    {
        count += n & 1;
        n >>= 1;
    }
    return count;
}
 
ll gcd(ll a, string b){
    ll res = 0;
    for (int i = 0; i < b.length(); i++){
        res = ((res * 10) + (b[i] - '0')) % a;
    }
    return __gcd(a,res);
}
 
void permutation(string s,ll i,ll j){
    if(i == j){
        cout<<s<<endl;
    }else{
        for(ll k=i;k<=j;k++){
            swap(s[i],s[k]);
            permutation(s,i+1,j);
            swap(s[i],s[k]);
        }
    }
}

void solve(){
    string s;cin>>s;
    permutation(s,0,s.length()-1);
}
 
