#define ll int long long

class Solution
{
    public:
    //Function to merge k sorted arrays.
    vector<int> mergeKArrays(vector<vector<int>> arr, int K)
    {
        //code here
        map<ll,ll> mp;
        for(int i=0;i<K;i++){
            for(int j=0;j<K;j++){
                mp[arr[i][j]]++;
            }
        }
        vector<int> ans;
        for(auto it:mp){
            ll x = it.second;
            while(x--){
                ans.push_back(it.first);
                // cout<<it.first<<" ";
            }
        }
        return ans;
    }
};

