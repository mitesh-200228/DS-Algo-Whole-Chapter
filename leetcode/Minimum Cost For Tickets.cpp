void solve(){
    ll n;cin>>n;
    std::vector<ll> days(n);
    unordered_set<ll> st;
    for(ll i=0;i<n;i++){
        cin>>days[i];
        st.insert(days[i]);
    }
    std::vector<ll> costs(3,0);
    for(ll i=0;i<3;i++){
        cin>>costs[i];
    }
    vector<ll> dp(366,0);
    for(ll i=1;i<=365;i++){
        if(st.find(i) == st.end()) dp[i] = dp[i-1];
        else dp[i] = min({dp[i-1] + costs[0],dp[max(0,i-7)] + costs[1],dp[max(0,i-30)] + costs[2]});
    }
    cout<<dp[365]<<endl;
}
 
